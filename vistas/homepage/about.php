                     <h2 style="text-align: justify;">¿Quién fue Fermín Toro?</h2>
                     <span></span>
                     <p style="text-align: justify;">
                        Fermín Toro Nace en Caracas el 1806 y muere en 1865. Fue un Escritor, diplomático y político venezolano. De formación autodidacta, 
                        con sólidos y amplios conocimientos en 1828 ingresó en la administración pública 
                        y desempeñó cargos aduaneros en La Guaira y Margarita. Fue diputado al Congreso 
                        Nacional en 1831.
                     </p>
                     <p style="text-align: justify;">
                        A partir de 1837 empezó a escribir en el diario caraqueño El Liberal con los 
                        seudónimos de Emiro Kastos y Jocasias. Viajó a Londres en 1839 como secretario 
                        de la misión diplomática presidida por Alejo Fortique, producto de su experiencia 
                        londinense fue la publicación por entregas de su novela Los mártires (1842), 
                        considerada la primera de su género en el pais, posteriormente publicaria otras novelas.
                     </p>
                     <p style="text-align: justify;">
                        A partir de 1844 desempeñó varias misiones diplomáticas en España, Francia e 
                        Inglaterra, de regreso al pais, en 1847 fue nombrado ministro de Hacienda. Designado 
                        diputado por Caracas al Congreso Nacional, renunció a esta representación tras los 
                        sucesos del 24 de enero de 1848, y no regresó hasta 1858, año en que, derrocado Monagas. presidió la Convención Nacional de Valencia. Nuevas misiones diplomáticas lo llevaron de regreso a Europa en 1860. Ya retirado de la actividad pública, se dedico al estudio de la botánica y de las lenguas indigenas.
                     </p>
